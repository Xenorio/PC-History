<div class="section">
    <h2>Linux Heute</h2>
    <h3>
        Während Linux in seinen Anfängen ein System für sehr fortgeschrittene
        Benutzer war, hat es sich mittlerweile so weit entwickelt dass jeder
        durchschnittliche Nutzer problemlos Linux installieren und benutzen
        kann. <br /><br />

        Linux ist auch in vielen anderen Bereichen als nur Heimcomputern in
        Benutzung. Die meisten Server laufen auf Linux, so wie viele
        verschiedene Geräte bei denen man nicht direkt an Computer denkt.
        Smartphones, Autos, Internet of Things Geräte, sogar die Menütafeln in
        Fast-Food Ketten. Es ist so gut wie unmöglich noch nie mit Linux in
        Kontakt gekommen zu sein, die meisten haben es nur nicht gemerkt. <br><br>
        
        Es gibt verschiedene sogenannte Distributionen von Linux. Jede Distribution
        ist sozusagen ihr eigenes Betriebssystem, welches verschiedene
        Standardprogramme mitliefert. Für Einsteiger gibt es z.B. Linux Mint,
        Pop!_OS, oder Fedora. Fortgeschrittene Benutzer können in Richtung Arch
        Linux gehen, wo man sich sein System komplett selbst zusammenbaut.
        Aktuell gibt es über 600 aktive Distributionen.
    </h3>
