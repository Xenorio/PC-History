<div class="section">
    <h2>Linus Torvalds und der Linux Kernel</h2>
    <h3>
        Um 1991 war Stallman fast damit fertig, die verschiedenen UNIX Programme
        zu ersetzen. Was jedoch noch fehlte war der Kernel, das Programm das die
        Systemressourcen verwaltet und den verschiedenen anderen Programmen
        zuweist. Ohne Kernel war GNU kein Betriebssystem, sondern lediglich eine
        Sammlung von Programmen. <br /><br />

        Linus Torvalds, damals Student an der Universität von Helsinki, hatte
        ein kleines Hobby-Projekt. Er wollte die Umgebung die er von den großen
        Universitätsrechnern gewöhnt war auf seinem eigenen Heimcomputer
        replizieren. Dazu schrieb er seinen eigenen Kernel, den er Linux nannte.
        <br /><br />

        Ende 1991 war Torvalds bei einem Vortrag von Stallman, bei dem er über
        seine Idee der freien Software redete. Torvalds war von dieser Idee so
        angetan, dass er seinen Linux Kernel unter der damals aktuellen GPLv2
        Lizenz veröffentlichte. Nun gab es einen freien Kernel, der in
        Verbindung mit GNU benutzt werden konnte. Endlich war GNU, bzw nun
        GNU/Linux ein vollwertiges Betriebssystem.
    </h3>

