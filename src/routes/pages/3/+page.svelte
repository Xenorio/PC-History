    <h2>Richard Stallman und GNU</h2>

    <h3>
        Richard Matthew Stallman trat 1971 dem MIT Artificial Intelligence Lab
        bei. Dort traf er auf gleichgesinnte Hacker, mit denen er das humorvoll
        benannte "Incompatible Timesharing System" Betriebssystem als Gegensatz
        zum ebenfalls im MIT entwickelten "Compatible Timesharing System"
        entwickelte. <br /><br />

        Während Stallman's Zeit beim MIT traf er auf immer mehr Komplikationen
        mit seiner eigenen Philosophie. Computer wurden mit Passwörtern
        versehen, und immer mehr Software wurde proprietär. Stallman war der
        Meinung, dass niemand Kontrolle über den Benutzer eines Computers
        ausüben können sollte. <br /><br />

        Aufgrund seiner Erfahrung in der Entwicklung von Betriebssystemen, kam
        Stallman auf die Idee ein neues, eigenes System ganz nach seinen
        Vorstellungen zu entwickeln. Dieses System sollte völlig Open-Source
        sein, und dem Nutzer volle Kontrolle zurückgeben. <br /><br />

        In 1984, ein Jahr vor der Erscheinung von Windows 1.0, fing Stallman mit
        der Entwicklung seines Systems an. Er nannte es GNU, eine rekursive
        Abkürzung für "GNU's Not UNIX". Damit wollte er aussagen, dass GNU dem
        damals vorherrschenden und proprietären UNIX-System ähnlich war, jedoch
        von Grund auf neu geschrieben wurde.
    </h3>

