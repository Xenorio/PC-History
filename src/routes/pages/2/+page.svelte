<h1 class="title">Wie Windows die Welt eroberte</h1>

<div class="container">
    <ul class="timeline">
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">MS-DOS - Ein System für Menschen</h5>
            <p class="text-muted mb-2 fw-bold">1981</p>
            <p class="text-muted">
                MS-DOS war das erste Betriebssystem von Microsoft, und wurde für
                den IBM PC gebaut. Es hatte noch keine grafische Oberfläche, war
                aber weitaus benutzerfreundlicher als frühere Systeme, welche
                hauptsächlich für den Business Bereich gebaut wurden. Mit MS-DOS
                konnte jedermann in die Welt der Computer einsteigen.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/7/71/MS-DOS_logo.svg/65px-MS-DOS_logo.svg.png"
                alt=""
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows 1.0</h5>
            <p class="text-muted mb-2 fw-bold">1985</p>
            <p class="text-muted">
                Mit Windows 1.0 brachte Microsoft die Anfänge der heutigen
                grafischen Oberflächen auf den Markt. Es war noch kein
                vollwertiges Betriebssystem, sondern sozusagen eine Erweiterung
                die auf MS-DOS lief. Tatsächlich war Windows 1.0 eher unbeliebt,
                da Nutzer noch nicht an grafische Oberflächen gewöhnt waren und
                es ihnen kostbare Systemressourcen aufgefressen hat.
            </p>
            <img
                src="https://www.howtogeek.com/wp-content/uploads/2020/11/windows_1_Screenshot.png?trim=1,1&bg-color=000&pad=1,1"
                alt=""
                class="big"
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows 2.0</h5>
            <p class="text-muted mb-2 fw-bold">1987</p>
            <p class="text-muted">
                Windows 2.0 war eine große Verbesserung zu Windows 1.0; Fenster
                konnten nun überlappen, es wurden bis zu 256 Farben unterstützt,
                und es war das erste Windows mit Word und Excel.
            </p>
            <img
                src="https://2.bp.blogspot.com/-ghFzxK3G6NI/VtNScTSbhJI/AAAAAAAAAGc/VFD7Pt_l3cs/s1600/win203.gif"
                alt=""
                class="big"
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows 3.0</h5>
            <p class="text-muted mb-2 fw-bold">1990</p>
            <p class="text-muted">
                Windows 3.0 brachte weitere revolutionäre änderungen. Die
                Oberfläche war nun Icon-Basiert, CD-ROM Laufwerke wurden nun
                unterstützt, es konnte Audio ausgeben werden, Multitasking wurde
                stark verbessert, und es wurden Spiele wie Minesweeper
                mitgeliefert. Mit Windows 3.1 wurde außerdem Unterstützung für
                Drag&Drop eingeführt.
            </p>
            <img
                src="https://cdn.neow.in/news/images/uploaded/2015/05/1_win30.jpg"
                alt=""
                class="big"
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows NT 3.1</h5>
            <p class="text-muted mb-2 fw-bold">1993</p>
            <p class="text-muted">
                Windows NT 3.1 war mit regulärem Windows 3.1 gleichzusetzen,
                ersetzte aber MS-DOS mit dem neuen NT Kernel, auf dessen
                Weiterentwicklungen heutige Windows Versionen immernoch
                basieren.
            </p>
            <img
                src="https://2.bp.blogspot.com/_LcTCyiiyIUw/TFdsMVMf-fI/AAAAAAAAAC8/FDJZWXgCjnU/s320/windowsnt31logo.gif"
                alt=""
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows 95</h5>
            <p class="text-muted mb-2 fw-bold">1995</p>
            <p class="text-muted">
                Mit Windows 95 wurden viele Grundsteine für heutiges Windows
                gelegt. Es war zwar noch MS-DOS basiert, brachte aber viele
                Funktionen welche heute unwiederruflich zu Windows gehören, z.B.
                das Start Menü und die Taskleiste. Mit späteren Updates kam
                außerdem Unterstützung für USB hinzu.
            </p>
            <img
                src="https://62e528761d0685343e1c-f3d1b99a743ffa4142d9d7f1978d9686.ssl.cf2.rackcdn.com/files/92472/area14mp/image-20150820-32485-ym567.png"
                alt=""
                class="big"
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows 98</h5>
            <p class="text-muted mb-2 fw-bold">1998</p>
            <p class="text-muted">
                Windows 98 sollte eine Erweiterung zu 95 darstellen, die auf
                verbesserten Zugang zum Internet ausgelegt war. Es war jedoch so
                Fehleranfällig, dass Microsoft ein Jahr später dazu gezwungen
                war Windows 98 "Second Edition" rauszubringen. Die meisten
                Nutzer konnten jedoch nicht überzeugt werden Windows 98 zu
                installieren.
            </p>
            <img
                src="https://images-wixmp-ed30a86b8c4ca887773594c2.wixmp.com/f/4989c596-7ba0-4e17-8b98-7c338208ab64/daobodr-7dc4c4b1-7bd3-4b50-87bb-c987e2c4dfb7.jpg?token=eyJ0eXAiOiJKV1QiLCJhbGciOiJIUzI1NiJ9.eyJzdWIiOiJ1cm46YXBwOiIsImlzcyI6InVybjphcHA6Iiwib2JqIjpbW3sicGF0aCI6IlwvZlwvNDk4OWM1OTYtN2JhMC00ZTE3LThiOTgtN2MzMzgyMDhhYjY0XC9kYW9ib2RyLTdkYzRjNGIxLTdiZDMtNGI1MC04N2JiLWM5ODdlMmM0ZGZiNy5qcGcifV1dLCJhdWQiOlsidXJuOnNlcnZpY2U6ZmlsZS5kb3dubG9hZCJdfQ.19QC3CkGVgTj7RdWWtd8kvfFFD-uwTWdI-Rqp3mbj4I"
                alt=""
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows XP</h5>
            <p class="text-muted mb-2 fw-bold">2001</p>
            <p class="text-muted">
                Windows XP ist das wohl bekannteste Windows aller Zeiten. Mit XP
                wurde MS-DOS komplett abgeschafft, und mit dem NT Kernel
                ersetzt. Ab jetzt war jedes Windows ein vollwertiges
                Betriebssystem. Es unterstütze 64-Bit Prozessoren, machte einen
                riesigen Schritt im Aussehen der GUI, und war weitaus stabiler
                und performanter. XP war außerdem das erste Windows mit einem
                Lizenzschlüssel-System.
            </p>
            <img
                src="https://cdn.arstechnica.net/wp-content/uploads/2014/04/Windows_XP_SP3.png"
                alt=""
                class="big"
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows Vista</h5>
            <p class="text-muted mb-2 fw-bold">2007</p>
            <p class="text-muted">
                Windows Vista war eines der Problemkinder von Microsoft. Viele
                Hintergrundfunktionen wurden von Grund auf neu entwickelt und
                stark modernisiert, jedoch war diese Modernisierung <strong
                    >zu</strong
                > modern. Die meisten damals erhältlichen Rechner hatten viel zu
                wenig Leistung um Vista optimal zu unterstützen, wodurch Vista von
                Nutzern als instabil abgestempelt wurde und floppte.
            </p>
            <img
                src="https://stock.wikimini.org/w/images/1/11/Windows-Vista-interface.png"
                alt=""
                class="big"
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows 7</h5>
            <p class="text-muted mb-2 fw-bold">2009</p>
            <p class="text-muted">
                Windows 7 war sehr ähnlich zu Vista, aber weitaus performanter.
                Außerdem hatten PC Hersteller nun genug Zeit gehabt um mit den
                Voraussetzungen hinterher zu kommen.
            </p>
            <img
                src="https://i.ytimg.com/vi/megiRyD9HxU/maxresdefault.jpg"
                alt=""
                class="big"
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows 8</h5>
            <p class="text-muted mb-2 fw-bold">2012</p>
            <p class="text-muted">
                Windows 8 war ein eher verwirrendes Betriebssystem. Microsoft
                versuchte ein einheitliches System für reguläre PCs und
                Mobilgeräte zu schaffen, wobei die Nutzerfreundlichkeit jedoch
                stark litt. Für PCs war es zu Mobile-Basiert, und für
                Mobilgeräte zu PC-Basiert. Viele Nutzer übersprangen Windows 8
                komplett, so wie Microsoft selbst Windows 9 übersprungen hat und
                direkt zu 10 ging.
            </p>
            <img
                src="https://www.trustedreviews.com/wp-content/uploads/sites/54/2013/10/Modern-UI-2-2.png"
                alt=""
                class="big"
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows 10</h5>
            <p class="text-muted mb-2 fw-bold">2015</p>
            <p class="text-muted">
                Mit Windows 10 nahm Microsoft das Mobile-First Konzept von
                Windows 8 zurück, und modernisierte gleichzeitig immens. Windows
                10 war eigentlich als die letzte Version von Windows angedacht,
                welche bis ans Ende aller Tage Aktualisierungen erhalten sollte.
                Diese Idee wurde jedoch wieder verworfen.
            </p>
            <img
                src="https://notentirelyuseless.com/wp-content/uploads/VirtualBox_Windows-10_15_05_2016_19_52_53-1024x768.png"
                alt=""
                class="big"
            />
        </li>
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Windows 11</h5>
            <p class="text-muted mb-2 fw-bold">2021</p>
            <p class="text-muted">
                Windows 11 ist die momentan aktuellste Version von Windows.
                Grundsätzlich ist es sehr ähnlich zu Windows 10, bringt aber
                einige Hintergrundfunktionen mit sich, welche für viel negatives
                Feedback gesorgt haben. z.B. setzt Microsoft nun einen TPM
                (Trusted Platform Module) Chip voraus, welchen viele Mainboards
                nicht verbaut hatten. Einige Design-Entscheidungen wie die
                zentrierte Taskleiste finden auch nur sehr wenig Anklang.
            </p>
            <img
                src="https://static1.xdaimages.com/wordpress/wp-content/uploads/2021/06/Windows-11-11.jpg?q=50&fit=crop&w=1500&dpr=1.5"
                alt=""
                class="big"
            />
        </li>
    </ul>
</div>

