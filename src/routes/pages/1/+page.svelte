<h1 class="title">Von mechanischen Rechenmaschinen zum ersten PC</h1>

<div class="container">
    <ul class="timeline">
        <li class="timeline-item mb-5">
            <h5 class="fw-bold">
                Pascaline - Erste mechanische Rechenmaschine
            </h5>
            <p class="text-muted mb-2 fw-bold">1642</p>
            <p class="text-muted">
                Die Pascaline, erfunden von Blaise Pascal, war die erste
                mechanische Rechenmaschine. Sie konnte lediglich 2 Nummern
                addieren oder subtrahieren.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/8/80/Arts_et_Metiers_Pascaline_dsc03869.jpg/220px-Arts_et_Metiers_Pascaline_dsc03869.jpg"
                alt=""
            />
        </li>

        <li class="timeline-item mb-5">
            <h5 class="fw-bold">
                Tabelliermaschine - Erste elektronische Datenverarbeitung
            </h5>
            <p class="text-muted mb-2 fw-bold">1887</p>
            <p class="text-muted">
                Die Tabelliermaschine, erfunden von Herman Hollerith, konnte
                Lochkarten auslesen und verarbeiten. Ursprünglich wurde sie
                benutzt, um Volkszählungen in den USA effizienter zu machen.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/4/4e/HollerithMachine.CHM.jpg/220px-HollerithMachine.CHM.jpg"
                alt=""
            />
        </li>

        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Turingmaschine - Universeller Computer</h5>
            <p class="text-muted mb-2 fw-bold">1936</p>
            <p class="text-muted">
                Der Mathematiker Alan Turing erdachte das theoretische Konzept
                der Turingmaschine. Dabei handelt es sich um ein abstraktes
                Gerät, bestehend aus einem Lese- und Schreibkopf, und einem
                unendlich langen Speicherband. Diese Machine wäre dazu in der
                lage, alles berechenbare zu berechnen.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/c/c4/Turingmaschine.svg/400px-Turingmaschine.svg.png"
                style="background-color: white;"
                alt=""
            />
        </li>

        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Turing-Bombe - Mathematische Kriegsführung</h5>
            <p class="text-muted mb-2 fw-bold">1940</p>
            <p class="text-muted">
                Die Turing-Bombe war ein auf der Arbeit von Alan Turing
                basierendes Gerät, welches von den Briten zur Entschlüsselung
                der von der Enigma Maschine verschlüsselten deutschen
                Funksprüche im 2. Weltkrieg benutzt wurde. Sie bestand aus
                mehreren Walzen, welche die Enigma Maschine emulierten.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/b/b1/RebuiltBombeFrontView.jpg/220px-RebuiltBombeFrontView.jpg"
                alt=""
            />
        </li>

        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Zuse Z3 - Erster funktionsfähiger Computer</h5>
            <p class="text-muted mb-2 fw-bold">1941</p>
            <p class="text-muted">
                Die deutschen Inginieure Konrad Zuse und Helmut Schreyer
                konstruierten ab 1938 die Zuse Z3, und stellten sie 1941 vor.
                Sie war die erste frei programmierbare Rechenmaschine.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/4/4c/Z3_Deutsches_Museum.JPG/220px-Z3_Deutsches_Museum.JPG"
                alt=""
            />
        </li>

        <li class="timeline-item mb-5">
            <h5 class="fw-bold">ENIAC - Die echte Turingmaschine</h5>
            <p class="text-muted mb-2 fw-bold">1941</p>
            <p class="text-muted">
                Der ENIAC, oder "Electronic Numerical Integrator and Computer",
                war die erste mit dem Konzept der Turingmaschine
                gleichzusetzende Apparatur. Er bestand hauptsächlich aus
                Vakuumröhren, zählte in binär, und war der erste digitale
                Computer. Benutzt wurde er von der US-Armee zur
                Ballistikberechnung.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/4/4e/Eniac.jpg/220px-Eniac.jpg"
                alt=""
            />
        </li>

        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Der erste Computer Bug</h5>
            <p class="text-muted mb-2 fw-bold">1947</p>
            <p class="text-muted">
                Grace Hopper, leitende Programmiererin des Harvard Mark II
                Computers, erfand den Begriff des Computer Bugs. Einer ihrer
                Techniker fand eine Motte (Bug = Käfer) in einem der Relais der
                Maschine. Hopper klebte die Motte in ihr Logbuch, mit dem Satz
                "First actual case of bug being found". Heute wird der Begriff
                als Bezeichnung für einen Fehler in einem Computersystem oder
                Programm verwendet.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/f/ff/First_Computer_Bug%2C_1945.jpg/220px-First_Computer_Bug%2C_1945.jpg"
                alt=""
            />
        </li>

        <li class="timeline-item mb-5">
            <h5 class="fw-bold">TRADIC - Jetzt mit Transistoren!</h5>
            <p class="text-muted mb-2 fw-bold">1954</p>
            <p class="text-muted">
                Der TRADIC, oder "Transistorized Airborne Digital Computer", war
                der erste auf Transistoren basierende Computer. Er war weitaus
                kleiner und schneller als seine Vorgänger. Während der ENIAC
                einen ganzen Raum einnahm und ungefähr 5000 Operationen pro
                Sekunde schaffte, war der TRADIC so groß wie ein Kleiderschrank
                und schaffte bis zu einer Millionen Operationen pro Sekunde.
                Heute basieren alle Computer (bis auf Quantencomputer) auf
                Transistoren.
            </p>
            <img
                src="https://elredondelito.es/wp-content/uploads/2020/02/tradic-felker-harris.jpg"
                alt=""
            />
        </li>

        <li class="timeline-item mb-5">
            <h5 class="fw-bold">
                Integrated Circuit - Schaltkreise im Kleinformat
            </h5>
            <p class="text-muted mb-2 fw-bold">1958</p>
            <p class="text-muted">
                Jack Kilby entwickelte in 1958 den ersten Integrated Circuit
                (IC), zu deutsch "Integrierter Schaltkreis". Dabei handelt es
                sich um das Konzept, elektronische Komponenten auf einem
                "handlichen" Chip unterzubringen. Daraus haben sich die heutigen
                Prozessoren entwickelt.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/e/ea/Replica_IC.png/290px-Replica_IC.png"
                alt=""
            />
        </li>

        <li class="timeline-item mb-5">
            <h5 class="fw-bold">Apple I - Computer für Zuhause</h5>
            <p class="text-muted mb-2 fw-bold">1976</p>
            <p class="text-muted">
                Der Apple I wird allgemein als erster PC angesehen, wobei der
                Begriff "Personal Computer" erst 1981 von IBM gemünzt wurde.
                Entgegen der gängigen Meinung, wurde er von Steve Wozniak
                entwickelt. Steve Jobs kam erst später dazu. Der Apple I bestand
                nur aus einer Platine, "Zubehör" wie Netzteile, Gehäuse, etc
                musste man sich selbst besorgen und zusammenbauen.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/2/25/CHM_Apple_I.JPG/220px-CHM_Apple_I.JPG"
                alt=""
            />
        </li>

        <li class="timeline-item mb-5">
            <h5 class="fw-bold">IBM Personal Computer - Das ganze Paket</h5>
            <p class="text-muted mb-2 fw-bold">1981</p>
            <p class="text-muted">
                Mit dem Personal Computer von IBM konnten Nutzer direkt einen
                vollständigen Computer kaufen, ohne erst daran rumschrauben zu
                müssen. Mit ihm begannen viele Standards die heutzutage
                selbstverständlich sind, z.B. Erweiterungskarten. Mit dem Intel
                8088 Mikroprozessor wurde auch die x86 Prozessorarchitektur
                massentauglich gemacht. Mit einem Preis von $5038 (der Inflation
                angepasst) war er zwar noch recht teuer, aber nicht völlig
                außerhalb der Reichweite des durchschnittlichen Verbrauchers.
            </p>
            <img
                src="https://upload.wikimedia.org/wikipedia/commons/thumb/a/a6/IBM_PC-IMG_7271_%28transparent%29.png/220px-IBM_PC-IMG_7271_%28transparent%29.png"
                alt=""
            />
        </li>
    </ul>
