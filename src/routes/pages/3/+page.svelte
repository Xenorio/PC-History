    <h2>Free Software</h2>
    <h3>
        In 1985 gründete Stallman die sogenannte Free Software Foundation (FSF).
        Das ist eine Nonprofit-Organisation welche sich zum Ziel setzt freie
        Software zu fördern und in aller Munde zu bringen. <br /><br />

        Das "Free" in "Free Software" bezieht sich dabei nicht auf den Preis,
        sondern auf Freiheit. Ein Zitat von Stallman: "Free as in freedom, not
        free as in beer". Freie Software gibt ihren Nutzern 4 grundlegende
        Rechte: <br /><br />

        <strong>0.</strong><br />
        Das Recht, das Programm für jegliche Zwecke auszuführen <br /><br />

        <strong>1.</strong><br />
        Das Recht, den Quellcode einzusehen und zu ändern <br /><br />

        <strong>2.</strong><br />
        Das Recht, Kopien des Programms zu verbreiten <br /><br />

        <strong>3.</strong><br />
        Das Recht, modifizierte Versionen des Programms zu verbreiten <br /><br
        />

        Um diese Rechte festzusetzen und Entwicklern die Möglichkeit zu geben
        sie zu benutzen, schrieb Stallman die "GNU General Public License" oder
        GPL. Diese Lizenz kann jeder mit seinem eigenen Programm mitliefern, um
        Nutzern diese Rechte zu geben. Außerdem sorgt sie dafür, dass Kopien der
        Programme auch unter der GPL lizensiert werden müssen. Heutzutage ist
        die GPL in der 3. Version (GPLv3).
    </h3>

